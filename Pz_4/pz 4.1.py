#Дано вещественное число X и целое число N (> 0). Найти значение выражения X -X3/(3!) + X5/(5!) - ... + (-1)N-X2-N+1/((2-N+1)!) (N! = 12 ...N).
# Полученное число является приближенным значением функции sin в точке X.

while True:
    try:
        x = float(input("Введите вещественное число X: "))
        n = int(input("Введите целое число N (> 0): "))
        break
    except ValueError:
        print("Неправильно ввели")
if n > 0:
    sin = 0.0
    sign = 1  # Начальный знак для первого члена ряда
    factorial = 1  # Начальное значение факториала

    for i in range(n):
        # Вычисляем текущий член ряда
        if i > 0:
            factorial *= (2 * i) * (2 * i + 1)  # Обновляем факториал для (2i + 1)
        term = sign * (x ** (2 * i + 1)) / factorial
        sin += term
        sign *= -1  # Меняем знак на противоположный

    print(f"Приближенное значение sin({x}) с помощью {n} членов ряда: {sin}") #выводим результат через f
else:
    print("N должно быть больше 0.")
