#Дано вещественное число X и целое число N (> 0). Найти значение выражения X -X3/(3!) + X5/(5!) - ... + (-1)N-X2-N+1/((2-N+1)!) (N! = 12 ...N).
# Полученное число является приближенным значением функции sin в точке X.

def mate_sin(x, n):
    sin = 0.0
    sign = 1  # Начальный знак для первого члена ряда
    factorial = 1  # Начальное значение факториала

    for i in range(n):
        if i > 0:
            factorial *= (2 * i) * (2 * i + 1)  # Обновляем факториал для (2i + 1)
        sin += sign * (x ** (2 * i + 1)) / factorial
        sign *= -1  # Меняем знак на противоположный

    return sin

# Ввод данных
while True:
    x = input("Введите вещественное число X: ")
    n = input("Введите целое число N (> 0): ")

    try:
        x = float(x)
        n = int(n)

        if n <= 0:
            print("Ошибка ввода: N должно быть больше 0.")
            continue

        break
    except ValueError:
        print("Ошибка ввода: пожалуйста, введите корректные значения.")

# Вычисление и вывод результата
result = mate_sin(x, n)
print(f"Приближенное значение sin({x}) с помощью {n} членов ряда: {result}")

